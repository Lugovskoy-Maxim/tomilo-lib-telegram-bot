/**
 * Команда /help и кнопка "Помощь"
 */
function setupHelpCommand(bot) {
    bot.help((ctx) => {
        ctx.reply(
            'Я бот для чтения манги и новелл с TOMILO LIB.\n\n' +
            '• Каталог — список тайтлов по страницам.\n' +
            '• Поиск — введите название и выберите тайтл.\n' +
            '• У любого тайтла нажмите «Выбрать главу» — откроется список глав. Выберите главу для чтения (PDF).\n' +
            '• Новые главы — лента последних обновлений.\n' +
            '• Teletype — если у тайтла или главы есть статья в Teletype, появится кнопка «Читать в Telegram»: материал откроется прямо в Telegram без перехода в браузер.\n\n' +
            'Команды:\n' +
            '/search — поиск тайтлов\n' +
            '/catalog — каталог\n' +
            '/new — новые главы\n' +
            '/help — эта справка'
        );
    });
}

module.exports = { setupHelpCommand };
